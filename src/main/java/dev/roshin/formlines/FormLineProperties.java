package dev.roshin.formlines;


/**
 * The FormLineProperties class encapsulates properties for a form line element.
 *
 */
public class FormLineProperties {
    private String valueLabel;
    private String valueVal;
    private String style;

    public String getValueLabel() {
        return valueLabel;
    }

    public void setValueLabel(String valueLabel) {
        this.valueLabel = valueLabel;
    }

    public String getValueVal() {
        return valueVal;
    }

    public void setValueVal(String valueVal) {
        this.valueVal = valueVal;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }
}